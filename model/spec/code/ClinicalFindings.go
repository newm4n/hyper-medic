func FindClinicalFindings(filter string) []ClinicalFindings {
	ret := make([]ClinicalFindings, 0)
	for _, at := range AllClinicalFindings() {
		if strings.ToLower(at.String())[0:len(filter)] == strings.ToLower(filter) {
			ret = append(ret, at)
		}
	}
	return ret
}

