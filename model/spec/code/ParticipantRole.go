func FindParticipantRole(filter string) []ParticipantRole {
	ret := make([]ParticipantRole, 0)
	for _, at := range AllParticipantRole() {
		if strings.ToLower(at.String())[0:len(filter)] == strings.ToLower(filter) {
			ret = append(ret, at)
		}
	}
	return ret
}

